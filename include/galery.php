<script>
  const modal = document.getElementById('studentModal');
  const modalBox = document.getElementById('modalBox');
  const closeBtn = document.getElementById('closeModal');

  function openModal(card) {
    modal.classList.remove('hidden');
    
    // Reset & apply fade/scale animation
    setTimeout(() => {
      modalBox.classList.remove('opacity-0', 'scale-95');
      modalBox.classList.add('opacity-100', 'scale-100');
    }, 10);

    // Isi data dari card
    document.getElementById('modalName').innerText = card.dataset.name;
    document.getElementById('modalNim').innerText = "NIM: " + card.dataset.nim;
    document.getElementById('modalClass').innerText = "Kelas: " + card.dataset.class;
    document.getElementById('modalSkill').innerText = "Keahlian: " + card.dataset.skill;
    document.getElementById('modalPhoto').src = card.dataset.photo;
    document.getElementById('modalSocial').href = card.dataset.social;
    document.getElementById('modalWebsite').href = card.dataset.website;
  }

  function closeModal() {
    // Animasi keluar
    modalBox.classList.remove('opacity-100', 'scale-100');
    modalBox.classList.add('opacity-0', 'scale-95');

    // Delay hide modal
    setTimeout(() => {
      modal.classList.add('hidden');
    }, 300);
  }

  closeBtn.addEventListener('click', closeModal);

  // Tutup saat klik di luar modal box
  window.addEventListener('click', function (e) {
    if (e.target === modal) {
      closeModal();
    }
  });
</script>

